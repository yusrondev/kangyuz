    public function resetTask(User $user)
    {

        $code     = 0;
        $err_msg  = "successfully";
        
        if (!Task::where('user_id', $user->id)->delete()) {
        
            $code = 1;
            $err_msg = "Some problem";
        
        }

        $return = [
            "code"    => $code,
            "err_msg" => $err_msg,
        ];

        return json_encode($return);
    }
